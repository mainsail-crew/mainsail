// Precompiles the Lezer .grammar files to committed .parser.ts files.
// Run: node scripts/build-grammars.mjs
import { buildParserFile } from '@lezer/generator'
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const dir = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'plugins', 'lezer')

for (const name of ['gcode', 'jinja', 'klipperConfig']) {
    const grammar = join(dir, `${name}.grammar`)
    let src
    try {
        src = readFileSync(grammar, 'utf8')
    } catch {
        continue // grammar not written yet
    }
    const { parser, terms } = buildParserFile(src, {
        fileName: grammar,
        moduleStyle: 'es',
    })
    const header =
        '// @ts-nocheck\n// GENERATED by scripts/build-grammars.mjs from ' + name + '.grammar — do not edit.\n'
    writeFileSync(join(dir, `${name}.parser.ts`), header + parser)
    writeFileSync(join(dir, `${name}.terms.ts`), header + terms)
    console.log(`built ${name}.parser.ts`)
}
